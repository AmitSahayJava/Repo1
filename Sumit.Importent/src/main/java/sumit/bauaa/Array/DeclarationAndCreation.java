package sumit.bauaa.Array;
public class DeclarationAndCreation {
	public static void main(String[] args) {
		//One way
		int[] x=new int[3];
		x[0]=10;
		x[1]=14; 
		x[2]=18; 
		System.out.println(x[2]); //18
		//Other way
		String[] names={"Apple","Ball","Cat","Dog"};
		System.out.println(names[0]); //Apple		
	}
}
